/**
 * Operadores aritméticos tomam valores numéricos (sejam literais ou variáveis) 
 * como seus operandos e retornam um único valor númerico. 
 * 
 * Ordem de execução () ** * / % + -
 */
console.log(10 + "5"); // Concatenação

console.log(10 + 5); // Adição
console.log(10 - 5); // Subtração
console.log(10 * 5); // Multiplicação
console.log(10 / 5); // Divisão

console.log(10 ** 5); // Exponenciação ou potência
console.log(10 % 5); // Módulo: resto da divisão